class Node():
    """Uma classe de nó para A * Pathfinding"""

    def __init__(self, parent=None, position=None):
        self.parent = parent
        self.position = position

        self.g = 0
        self.h = 0
        self.f = 0

    def __eq__(self, other):
        return self.position == other.position


def astar(maze, start, end):
    """Retorna uma lista de tuplas como um caminho desde o início até o fim no labirinto."""

    # Cria o nó inicial e final
    start_node = Node(None, start)
    start_node.g = start_node.h = start_node.f = 0
    end_node = Node(None, end)
    end_node.g = end_node.h = end_node.f = 0

    # Inicializa as listas aberta e fechada
    open_list = []
    lista = []
    closed_list = []

    # Adiciona o nó inicial
    open_list.append(start_node)
    # Faz um loop até encontrar o fim
    while len(open_list) > 0:
        # Obtem o nó atual
        current_node = open_list[0]
        current_index = 0

        for index, item in enumerate(open_list):
            # print(item.position)
            if item.f < current_node.f:
                current_node = item
                # print(item.position)
                current_index = index

        # Pop atual fora da lista aberta, adicionar à lista fechada
        open_list.pop(current_index)
        closed_list.append(current_node)

        # Encontrou o objetivo
        if current_node == end_node:
            path = []
            current = current_node
            while current is not None:
                path.append(current.position)
                current = current.parent
            return path[::-1], lista[::1]  # Return reversed path

        # Gera filho
        children = []
        for new_position in [(0, -1), (0, 1), (-1, 0), (1, 0)]:  # Quadrados adjacentes

            # Obtem a posição do nó
            node_position = (
                current_node.position[0] + new_position[0], current_node.position[1] + new_position[1])

            # Verifica-se de esta dentro do alcance
            if node_position[0] > (len(maze) - 1) or node_position[0] < 0 or node_position[1] > (len(maze[len(maze)-1]) - 1) or node_position[1] < 0:
                continue

            # Verifica-se se a posicao é acessíveis
            if maze[node_position[0]][node_position[1]] == 0:
                continue

            # Cria novo nó
            new_node = Node(current_node, node_position)

            # Adiciona
            children.append(new_node)

        # Loop percorrendo as crianças
        for child in children:

            # A criança está na lista fechada
            if len([closed_child for closed_child in closed_list if closed_child == child]) > 0:
                continue

            # Crie os valores f, g e h
            child.g = current_node.g + 1
            child.h = ((child.position[0] - end_node.position[0]) **
                       2) + ((child.position[1] - end_node.position[1]) ** 2)
            child.f = child.g + child.h

            # O filho já está na lista aberta
            if len([open_node for open_node in open_list if child == open_node and child.g > open_node.g]) > 0:
                continue

            # Adiciona o filho à lista aberta
            open_list.append(child)
            lista.append(child)


